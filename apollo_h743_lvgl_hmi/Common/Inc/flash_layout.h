#ifndef FLASH_LAYOUT_H
#define FLASH_LAYOUT_H

#include <stdint.h>

#define BOOT_START_ADDR        0x08000000UL
#define BOOT_SIZE_BYTES        0x00020000UL
#define BOOT_END_ADDR          (BOOT_START_ADDR + BOOT_SIZE_BYTES - 1UL)

#define APP_SLOT_HEADER_SIZE   0x00000400UL
#define APP_TAG_SIZE_BYTES     64UL

#define APP_A_START_ADDR       0x08020000UL
#define APP_A_SIZE_BYTES       0x000E0000UL
#define APP_A_TAG_ADDR         (APP_A_START_ADDR + APP_SLOT_HEADER_SIZE - APP_TAG_SIZE_BYTES)
#define APP_A_RUN_ADDR         (APP_A_START_ADDR + APP_SLOT_HEADER_SIZE)
#define APP_A_MAX_SIZE_BYTES   (APP_A_SIZE_BYTES - APP_SLOT_HEADER_SIZE)

#define APP_B_START_ADDR       0x08100000UL
#define APP_B_SIZE_BYTES       0x00100000UL
#define APP_B_TAG_ADDR         (APP_B_START_ADDR + APP_SLOT_HEADER_SIZE - APP_TAG_SIZE_BYTES)
#define APP_B_RUN_ADDR         (APP_B_START_ADDR + APP_SLOT_HEADER_SIZE)
#define APP_B_MAX_SIZE_BYTES   (APP_B_SIZE_BYTES - APP_SLOT_HEADER_SIZE)

#define STM32H743_FLASH_START  0x08000000UL
#define STM32H743_FLASH_END    0x081FFFFFUL
#define STM32H743_FLASH_BANK1  0x08000000UL
#define STM32H743_FLASH_BANK2  0x08100000UL
#define STM32H743_FLASH_SECTOR_SIZE 0x00020000UL

#define APP_VECTOR_TABLE_SIZE  APP_SLOT_HEADER_SIZE
#define APP_VECTOR_TABLE_ALIGN APP_SLOT_HEADER_SIZE

#endif
